namespace Craft.Expressions;

/// <summary>
/// Defines constant values for expression operators.
/// </summary>
public static class ExpressionOperators
{
    /// <summary>
    /// Logical AND operator: &amp;&amp;
    /// </summary>
    public const string And = "&&";

    /// <summary>
    /// Logical OR operator: ||
    /// </summary>
    public const string Or = "||";

    /// <summary>
    /// Logical NOT operator: !
    /// </summary>
    public const string Not = "!";

    /// <summary>
    /// Equality operator: ==
    /// </summary>
    public const string Equal = "==";

    /// <summary>
    /// Inequality operator: !=
    /// </summary>
    public const string NotEqual = "!=";

    /// <summary>
    /// Greater than operator: &gt;
    /// </summary>
    public const string GreaterThan = ">";

    /// <summary>
    /// Greater than or equal operator: &gt;=
    /// </summary>
    public const string GreaterThanOrEqual = ">=";

    /// <summary>
    /// Less than operator: &lt;
    /// </summary>
    public const string LessThan = "<";

    /// <summary>
    /// Less than or equal operator: &lt;=
    /// </summary>
    public const string LessThanOrEqual = "<=";

    /// <summary>
    /// Dot operator for member access: .
    /// </summary>
    public const string Dot = ".";

    /// <summary>
    /// Comma separator: ,
    /// </summary>
    public const string Comma = ",";

    /// <summary>
    /// Opening parenthesis: (
    /// </summary>
    public const string OpenParen = "(";

    /// <summary>
    /// Closing parenthesis: )
    /// </summary>
    public const string CloseParen = ")";
}
