namespace Craft.UiComponents.Enums;

/// <summary>
/// Defines the duration presets for animations and transitions.
/// </summary>
public enum AnimationDuration
{
    /// <summary>
    /// Instant transition (0ms).
    /// </summary>
    None = 0,

    /// <summary>
    /// Extra fast transition (100ms).
    /// </summary>
    ExtraFast = 100,

    /// <summary>
    /// Fast transition (200ms).
    /// </summary>
    Fast = 200,

    /// <summary>
    /// Normal/default transition (300ms).
    /// </summary>
    Normal = 300,

    /// <summary>
    /// Slow transition (500ms).
    /// </summary>
    Slow = 500,

    /// <summary>
    /// Extra slow transition (700ms).
    /// </summary>
    ExtraSlow = 700
}
