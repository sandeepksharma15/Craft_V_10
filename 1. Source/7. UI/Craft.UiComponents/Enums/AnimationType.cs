namespace Craft.UiConponents.Enums;

/// <summary>
/// Defines the animation types available for UI components.
/// </summary>
public enum AnimationType
{
    /// <summary>
    /// No animation.
    /// </summary>
    None,

    /// <summary>
    /// Fade in/out animation.
    /// </summary>
    Fade,

    /// <summary>
    /// Slide animation.
    /// </summary>
    Slide,

    /// <summary>
    /// Scale/zoom animation.
    /// </summary>
    Scale,

    /// <summary>
    /// Collapse/expand animation.
    /// </summary>
    Collapse,

    /// <summary>
    /// Bounce animation effect.
    /// </summary>
    Bounce,

    /// <summary>
    /// Shake animation effect.
    /// </summary>
    Shake,

    /// <summary>
    /// Pulse animation effect.
    /// </summary>
    Pulse,

    /// <summary>
    /// Flip animation effect.
    /// </summary>
    Flip,

    /// <summary>
    /// Rotate animation effect.
    /// </summary>
    Rotate
}
