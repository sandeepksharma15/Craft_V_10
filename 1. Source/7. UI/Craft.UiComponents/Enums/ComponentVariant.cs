namespace Craft.UiConponents.Enums;

/// <summary>
/// Defines the visual style variants available for UI components.
/// </summary>
public enum ComponentVariant
{
    /// <summary>
    /// Default variant with standard styling.
    /// </summary>
    Default,

    /// <summary>
    /// Primary variant, typically used for main actions.
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary variant, used for less prominent actions.
    /// </summary>
    Secondary,

    /// <summary>
    /// Success variant, indicates positive actions or states.
    /// </summary>
    Success,

    /// <summary>
    /// Warning variant, indicates cautionary actions or states.
    /// </summary>
    Warning,

    /// <summary>
    /// Danger variant, indicates destructive or critical actions.
    /// </summary>
    Danger,

    /// <summary>
    /// Info variant, used for informational content.
    /// </summary>
    Info,

    /// <summary>
    /// Light variant with minimal styling.
    /// </summary>
    Light,

    /// <summary>
    /// Dark variant with dark background styling.
    /// </summary>
    Dark,

    /// <summary>
    /// Outlined variant with border and transparent background.
    /// </summary>
    Outlined,

    /// <summary>
    /// Text variant with no background or border.
    /// </summary>
    Text
}
